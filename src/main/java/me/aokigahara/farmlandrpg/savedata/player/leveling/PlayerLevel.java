package me.aokigahara.farmlandrpg.savedata.player.leveling;

public class PlayerLevel {
    private int exp = 0;
    private int level = 1;



    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
